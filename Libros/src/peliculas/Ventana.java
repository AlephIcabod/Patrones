    private void actualizarTabla(){
        String filtroGenero=(String)fGeneroCb.getSelectedItem();
        String filtroNombre=filtroNombreT.getText();
        DefaultTableModel model=new DefaultTableModel(new Object[]{"Id","Titulo","Genero","Fecha estreno","Director"},0);
            for(int i=0;i<peliculas.size();i++){
                if ((filtroGenero=="-"||peliculas.get(i).getGenero()==filtroGenero)
                   &&(filtroNombre.trim().isEmpty()||peliculas.get(i).getTitulo().contains(filtroNombre))){
                    model.addRow(new Object[]{peliculas.get(i).getId(),
                                              peliculas.get(i).getTitulo(),
                                            peliculas.get(i).getGenero(),
                                            peliculas.get(i).getFechaEstreno(),
                                            peliculas.get(i).getDirector()});
                }
        }
            tabla.setModel(model);
    }
